export type IconName = keyof typeof PATHS;

/** 產生單顆 SVG 圖標字串（stroke: currentColor → 跟著文字色走） */
export function iconHtml(name: IconName, size = 16): string {
  return (
    `<svg class="ic" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" ` +
    `stroke="currentColor" stroke-width="1.75" stroke-linecap="round" stroke-linejoin="round" ` +
    `aria-hidden="true">${PATHS[name]}</svg>`
  );
}

/** 把文件中所有 <span data-icon="xxx"> 填入對應 SVG（靜態 HTML 的圖標掛載） */
export function mountIcons(root: ParentNode = document): void {
  root.querySelectorAll<HTMLElement>('[data-icon]').forEach((el) => {
    const name = el.dataset['icon'] as IconName | undefined;
    if (!name || !(name in PATHS)) return;
    const size = Number(el.dataset['iconSize'] || 16);
    el.innerHTML = iconHtml(name, size);
  });
}
